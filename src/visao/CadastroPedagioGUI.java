    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        PedagioBEAN vb = new PedagioBEAN();
        vb.setPed_data(tfData.getText());
        vb.setPed_veicod(Integer.parseInt((String)jComboBox1.getSelectedItem()));
        vb.setPed_pracod(Integer.parseInt((String)jComboBox2.getSelectedItem()));
        pec.cadastraPedagio(vb);
        JOptionPane.showMessageDialog(this, "Cadastrado com sucesso!");
    }

    private void preencheComboBoxVeiculo(ArrayList<VeiculoBEAN> vb) {
        for (int i = 0; i < vb.size(); i++) {
            jComboBox1.addItem(String.valueOf(vb.get(i).getVeicod()));
        }
    }

    private void preencheComboBoxPraca(ArrayList<PracaBEAN> pb) {
        for (int i = 0; i < pb.size(); i++) {
            jComboBox2.addItem(String.valueOf(pb.get(i).getPracod()));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

