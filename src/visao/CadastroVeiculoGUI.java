    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (!jTextField2.getText().equals("")) {
            VeiculoBEAN vb = new VeiculoBEAN();
            //pega o dado selecionado do JComboBox (os dados do combo nao estao no BD pois foram definidos apriori no enunciado)
            vb.setVeitipo((String) comboTipo.getSelectedItem());
            vb.setVeivalor(Float.parseFloat(jTextField2.getText()));
            vc.cadastraVeiculo(vb);
            JOptionPane.showMessageDialog(this, "Cadastro feito com sucesso!!");
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, insira o tipo de veículo ou valor a ser pago!!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

